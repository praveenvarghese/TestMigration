scenario(
	"Customer-ticket#3901 - Asserting decimal values on a filtered WebUI tablet widget.",
	() => {
		loadPage("Main Project/Other Customer Bugs/c3901?table-v2=false");

		// Appling table column filter
		findWidget("c3901").getColFilter(0);
		getFilter().addFilter({ rule: "is greater than or equal (>=)", value: "0.001" });
		findDialog()
			.findButton("Apply and Close")
			.click();

		// Asserting data on the table, with its decimal values.
		findWidget("c3901")
			.getGridValues()
			.should.be.shallowDeepEqual([
				["0.00100000000000000000"],
				["0.01000000000000000000"],
				["0.10000000000000000000"],
				["1.00000000000000000000"],
				["10.00000000000000000000"],
				["100.00000000000000000000"],
				["1,000.00000000000000000000"],
				["10,000.00000000000000000000"],
				["100,000.00000000000000000000"],
				["1,000,000.00000000000000000000"],
				["10,000,000.00000000000000000000"],
				["100,000,000.00000000000000000000"],
				["1,000,000,000.00000000000000000000"],
				["10,000,000,000.00000000000000000000"],
				["100,000,000,000.00000000000000000000"],
				["1,000,000,000,000.00000000000000000000"],
				["10,000,000,000,000.00000000000000000000"],
				["100,000,000,000,000.00000000000000000000"],
				["1,000,000,000,000,000.00000000000000000000"],
				["10,000,000,000,000,000.00000000000000000000"],
				["100,000,000,000,000,000.00000000000000000000"],
				["1,000,000,000,000,000,000.00000000000000000000"],
				["10,000,000,000,000,000,000.00000000000000000000"],
				["100,000,000,000,000,000,000.00000000000000000000"],
			]);

		// Editing table column filter
		findWidget("c3901")
			.getTableFilterButton()
			.click();
		getFilter().editFilter({ index: "0", rule: "is less than (<)", value: "0.001" });
		findDialog()
			.findButton("Apply and Close")
			.click();

		// Asserting data on the table, with its decimal values.
		findWidget("c3901")
			.getGridValues()
			.should.be.shallowDeepEqual([
				["0.00000000000000000001"],
				["0.00000000000000000010"],
				["0.00000000000000000100"],
				["0.00000000000000001000"],
				["0.00000000000000010000"],
				["0.00000000000000100000"],
				["0.00000000000001000000"],
				["0.00000000000010000000"],
				["0.00000000000100000000"],
				["0.00000000001000000000"],
				["0.00000000010000000000"],
				["0.00000000100000000000"],
				["0.00000001000000000000"],
				["0.00000010000000000000"],
				["0.00000100000000000000"],
				["0.00001000000000000000"],
				["0.00010000000000000000"],
			]);
	}
);
