scenario("Validate filter option is displayed on an standard sidepanel page v2", () => {
	loadPage("Main Project/home");

	findWidget("home")
		.getSidepanels()
		.openSidepanelTab("Impact Score");

	findWidget("tableStandard")
		.getTableFilterButton()
		.should.not.be.eql("None");

	findWidget("tableStandard")
		.getGridValues()
		.should.be.shallowDeepEqual([
			["1,000.00", "1,000.00", "1,000.00", "1,000.00", "1,000.00"],
			["22.00", "22.00", "22.00", "22.00", "22.00"],
			["21.00", "21.00", "21.00", "21.00", "21.00"],
			["20.00", "20.00", "20.00", "20.00", "20.00"],
			["4.00", "4.00", "4.00", "4.00", "4.00"],
			["3.00", "3.00", "3.00", "3.00", "3.00"],
			["2.00", "2.00", "2.00", "2.00", "2.00"],
			["10,000.00", "1,000.00", "5,000.00", "1,000.00", "2,500.00"],
			["12,000.00", "4,000.00", "8,000.00", "3,000.00", "6,000.00"],
			["1,000.00", "-1,000.00", "1,000.00", "-1,000.00", "-1,000.00"],
			["22.00", "-22.00", "22.00", "22.00", "-22.00"],
			["10,000.00", "-1,000.00", "5,000.00", "1,000.00", "-2,500.00"],
		]);

	findWidget("tableStandard")
		.getTableFilterButton()
		.click();

	getFilter().clearAllFilters();

	findWidget("tableStandard")
		.getTableFilterButton()
		.should.be.eql("None");

	findWidget("tableStandard")
		.getGridValues()
		.should.be.shallowDeepEqual([
			["1,000.00", "1,000.00", "1,000.00", "1,000.00", "1,000.00"],
			["22.00", "22.00", "22.00", "22.00", "22.00"],
			["21.00", "21.00", "21.00", "21.00", "21.00"],
			["20.00", "20.00", "20.00", "20.00", "20.00"],
			["4.00", "4.00", "4.00", "4.00", "4.00"],
			["3.00", "3.00", "3.00", "3.00", "3.00"],
			["2.00", "2.00", "2.00", "2.00", "2.00"],
			["1.00", "1.00", "1.00", "1.00", "1.00"],
			["-1.00", "-1.00", "-1.00", "-1.00", "-1.00"],
			["-2.00", "-2.00", "-2.00", "-2.00", "-2.00"],
			["-3.00", "-3.00", "-3.00", "-3.00", "-3.00"],
			["-4.00", "-4.00", "-4.00", "-4.00", "-4.00"],
			["-20.00", "-20.00", "-20.00", "-20.00", "-20.00"],
			["-21.00", "-21.00", "-21.00", "-21.00", "-21.00"],
			["-22.00", "-22.00", "-22.00", "-22.00", "-22.00"],
		]);
});
